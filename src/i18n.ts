type TranslationKey = keyof (typeof translations)['pt-br']

// \u2500\u2500\u2500 t() helper \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500
/** Translate a key with optional variable interpolation. e.g. t('bet.placed', {bettor:'Juan'}) */
export function t(key: TranslationKey, vars?: Record<string, string | number>): string {
  const raw = translations[LANG][key] as string
  if (!vars) return raw
  return Object.entries(vars).reduce(
    (str, [k, v]) => str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v)),
    raw
  )
}
